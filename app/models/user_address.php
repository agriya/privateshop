    function checkShippingAddress($address,$uid = null)
    {
		return $this->find('count', array(
            'conditions' => array(
                'UserAddress.address' => $address,
                'UserAddress.user_id' => $uid,
            ) ,
            'recursive' => -1,
        ));
    }
}