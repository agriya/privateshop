            ) ,
            'full_name' => array(
                'rule1' => array(
                    'rule' => 'notempty',
                    'allowEmpty' => false,
                    'message' => __l('Required') ,
                ) ,
            ) ,
            'address' => array(
                'rule1' => array(
                    'rule' => 'notempty',
